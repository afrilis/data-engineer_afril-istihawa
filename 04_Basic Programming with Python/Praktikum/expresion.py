# Aritmatika
a = 2
b = 3

print(a + b) # penjumlahan
print(a - b) # pengurangan
print(a / b) # pembagian
print(a * b) # perkalian
print(a % b) # modulus atau hasil sisa bagi
print(a**b) # perpangkatan

# Komparasi
print (a == b)
print (a != b)
print (a < b)
print (a > b)
print (a <= b)
print (a >= b)

# Logic => and, or, not
c = True
d = False
if c and d:
    print(1)

if c or d:
    print(1)

if not c:
    print(1)

# Assignment
a += b # a = a + b
print(a)
